package Fundamentals;

import java.util.Scanner;

public class Boxes_Toys {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int t = sc.nextInt();
		int count = 0;
		while(t-->0) {
			
			int T = sc.nextInt();
			int c = sc.nextInt();
			
			if( c-T>=2);
			count++;
		}
		System.out.println(count);
		
	}

}
